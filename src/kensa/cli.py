def _harness_authenticity_warnings() -> list[str]:
    path = _harness_conftest_path()
    if path is None:
        return []
    try:
        source = path.read_text()
    except OSError as exc:
        return [f"Could not inspect Kensa harness authenticity: {exc}"]
    return _detect_known_suspicious_harness_patterns(source)


def _detect_known_suspicious_harness_patterns(source: str) -> list[str]:
    """Catch common harness footguns; prompts document the broader policy."""

    try:
        tree = ast.parse(source)
    except SyntaxError:
        return []

    warnings: list[str] = []
    if _defines_known_replacement_agent_pattern(tree):
        warnings.append(
            "Harness defines a fake, fallback, stub, or mock agent. Kensa evals should call "
            "the real local agent boundary and mock only external dependencies."
        )
    if _uses_agent_dunder_new(tree):
        warnings.append(
            "Harness bypasses an agent constructor with __new__. Prefer a real test factory "
            "or dependency injection so the agent under test is initialized normally."
        )
    if _has_module_not_found_replacement_fallback(tree):
        warnings.append(
            "Harness catches ModuleNotFoundError and substitutes a replacement agent. Missing "
            "real-agent dependencies should fail loudly or be installed before Kensa runs."
        )
    if _adds_coverage_option_shim(tree):
        warnings.append(
            "Harness registers pytest coverage options itself. Install the project's test "
            "dependencies or adjust pytest configuration instead of hiding a missing plugin."
        )
    if _swallows_broad_exception_silently(tree):
        warnings.append(
            "Harness silently swallows a bare or broad exception. Kensa setup failures should "
            "raise or include an explicit diagnostic."
        )
    if _uses_hardcoded_case_input_fallback(tree):
        warnings.append(
            "Harness appears to invent hard-coded fallback text for missing case input or "
            "messages. Malformed Kensa cases should fail loudly instead of running a "
            "synthetic eval."
        )
    return warnings


def _defines_known_replacement_agent_pattern(tree: ast.AST) -> bool:
    markers = ("fake", "fallback", "mock", "stub")
    for node in ast.walk(tree):
        if not isinstance(node, ast.ClassDef):
            continue
        name = node.name.lower()
        if name.endswith("agent") and any(marker in name for marker in markers):
            return True
    return False


def _uses_agent_dunder_new(tree: ast.AST) -> bool:
    for node in ast.walk(tree):
        if not isinstance(node, ast.Call):
            continue
        func = node.func
        if (
            isinstance(func, ast.Attribute)
            and func.attr == "__new__"
            and _is_agent_like_dunder_new_receiver(func.value)
        ):
            return True
    return False


def _is_agent_like_dunder_new_receiver(node: ast.expr) -> bool:
    if isinstance(node, ast.Name):
        return "agent" in node.id.lower()
    if isinstance(node, ast.Attribute):
        return "agent" in node.attr.lower()
    return False


def _has_module_not_found_replacement_fallback(tree: ast.AST) -> bool:
    for node in ast.walk(tree):
        if not isinstance(node, ast.ExceptHandler) or not _handles_module_not_found(node.type):
            continue
        if _mentions_replacement_agent(node):
            return True
    return False


def _handles_module_not_found(node: ast.expr | None) -> bool:
    if node is None:
        return False
    if isinstance(node, ast.Name):
        return node.id == "ModuleNotFoundError"
    if isinstance(node, ast.Tuple):
        return any(_handles_module_not_found(item) for item in node.elts)
    return False


def _mentions_replacement_agent(node: ast.AST) -> bool:
    markers = ("fake", "fallback", "mock", "stub")
    for child in ast.walk(node):
        name = ""
        if isinstance(child, ast.Name):
            name = child.id.lower()
        elif isinstance(child, ast.Attribute):
            name = child.attr.lower()
        if name.endswith("agent") and any(marker in name for marker in markers):
            return True
    return False


def _adds_coverage_option_shim(tree: ast.AST) -> bool:
    for node in ast.walk(tree):
        if not isinstance(node, ast.FunctionDef) or node.name != "pytest_addoption":
            continue
        for child in ast.walk(node):
            if isinstance(child, ast.Constant) and child.value in {"--cov", "--cov-report"}:
                return True
    return False


def _swallows_broad_exception_silently(tree: ast.AST) -> bool:
    for node in ast.walk(tree):
        if not isinstance(node, ast.ExceptHandler):
            continue
        if _handles_broad_exception(node.type) and _is_silent_exception_body(node.body):
            return True
    return False


def _handles_broad_exception(node: ast.expr | None) -> bool:
    if node is None:
        return True
    if isinstance(node, ast.Name):
        return node.id in {"BaseException", "Exception"}
    if isinstance(node, ast.Attribute):
        return (
            node.attr in {"BaseException", "Exception"}
            and isinstance(node.value, ast.Name)
            and node.value.id == "builtins"
        )
    if isinstance(node, ast.Tuple):
        return any(_handles_broad_exception(item) for item in node.elts)
    return False


def _is_silent_exception_body(body: list[ast.stmt]) -> bool:
    return bool(body) and all(_is_silent_exception_statement(statement) for statement in body)


def _is_silent_exception_statement(statement: ast.stmt) -> bool:
    if isinstance(statement, ast.Pass):
        return True
    if isinstance(statement, ast.Expr) and isinstance(statement.value, ast.Constant):
        return statement.value.value is Ellipsis
    if isinstance(statement, ast.Return):
        return statement.value is None or (
            isinstance(statement.value, ast.Constant) and statement.value.value in {None, False}
        )
    return isinstance(statement, ast.Continue)


def _uses_hardcoded_case_input_fallback(tree: ast.AST) -> bool:
    for node in ast.walk(tree):
        if not isinstance(node, ast.FunctionDef | ast.AsyncFunctionDef):
            continue
        if not _is_case_adapter_context(node):
            continue
        for child in ast.walk(node):
            if isinstance(child, ast.Return) and _is_hardcoded_case_input_value(child.value):
                return True
            if isinstance(child, ast.Call) and _is_get_call_with_hardcoded_text_default(child):
                return True
            if isinstance(child, ast.BoolOp) and _is_or_expression_with_hardcoded_text_default(
                child
            ):
                return True
            if isinstance(child, ast.IfExp) and _is_if_expression_with_hardcoded_text_default(
                child
            ):
                return True
    return False


def _is_case_adapter_context(node: ast.FunctionDef | ast.AsyncFunctionDef) -> bool:
    args = [*node.args.posonlyargs, *node.args.args, *node.args.kwonlyargs]
    if any(arg.arg in {"case", "row", "input", "message", "messages"} for arg in args):
        return True
    tokens = set(_name_tokens(node.name))
    return "kensa" in tokens or _is_inputish_name(node.name)


def _is_hardcoded_case_input_value(node: ast.expr | None) -> bool:
    if isinstance(node, ast.Constant) and isinstance(node.value, str):
        return bool(node.value.strip())
    if isinstance(node, ast.List | ast.Tuple | ast.Set):
        return len(node.elts) == 1 and _is_hardcoded_case_input_value(node.elts[0])
    return False


def _is_get_call_with_hardcoded_text_default(node: ast.Call) -> bool:
    if not isinstance(node.func, ast.Attribute) or node.func.attr != "get" or len(node.args) < 2:
        return False
    return _is_input_key_literal(node.args[0]) and _is_hardcoded_case_input_value(node.args[1])


def _is_input_key_literal(node: ast.expr) -> bool:
    if not isinstance(node, ast.Constant) or not isinstance(node.value, str):
        return False
    return node.value.lower() in {"content", "input", "message", "messages", "prompt", "query"}


def _is_or_expression_with_hardcoded_text_default(node: ast.BoolOp) -> bool:
    if not isinstance(node.op, ast.Or):
        return False
    return any(_is_inputish_expression(value) for value in node.values) and any(
        _is_hardcoded_case_input_value(value) for value in node.values[1:]
    )


def _is_if_expression_with_hardcoded_text_default(node: ast.IfExp) -> bool:
    if not _is_hardcoded_case_input_value(node.orelse):
        return False
    return _is_inputish_expression(node.body) or _is_inputish_expression(node.test)


def _is_inputish_expression(node: ast.expr) -> bool:
    if isinstance(node, ast.Name):
        return _is_inputish_name(node.id)
    if isinstance(node, ast.Attribute):
        return _is_inputish_name(node.attr) or _is_inputish_expression(node.value)
    if isinstance(node, ast.Subscript):
        return _is_inputish_expression(node.value) or _is_inputish_subscript(node.slice)
    if isinstance(node, ast.Call) and isinstance(node.func, ast.Attribute):
        return _is_inputish_name(node.func.attr) or _is_inputish_expression(node.func.value)
    if isinstance(node, ast.BoolOp):
        return any(_is_inputish_expression(value) for value in node.values)
    if isinstance(node, ast.UnaryOp):
        return _is_inputish_expression(node.operand)
    if isinstance(node, ast.Compare):
        return _is_inputish_expression(node.left) or any(
            _is_inputish_expression(comparator) for comparator in node.comparators
        )
    return False


def _is_inputish_subscript(node: ast.expr) -> bool:
    if isinstance(node, ast.Constant) and isinstance(node.value, str):
        return _is_inputish_name(node.value)
    return _is_inputish_expression(node)


def _is_inputish_name(name: str) -> bool:
    tokens = set(_name_tokens(name))
    return bool(
        tokens
        & {
            "content",
            "contents",
            "input",
            "inputs",
            "message",
            "messages",
            "prompt",
            "prompts",
            "query",
            "queries",
            "text",
            "texts",
        }
    )


def _name_tokens(name: str) -> tuple[str, ...]:
    normalized = "".join(character.lower() if character.isalnum() else "_" for character in name)
    return tuple(part for part in normalized.split("_") if part)


def _doctor_redaction_report() -> dict[str, Any]:
    missing = redact.missing_redaction_dependencies()
    settings = _read_settings()
    readiness: redact.RedactionReadiness | None = None
    readiness_error: str | None = None
    try:
        readiness = redact.assert_redaction_ready()
    except redact.RedactionError as exc:
        readiness_error = str(exc)
        try:
            readiness = redact.read_redaction_readiness()
        except redact.RedactionError:
            readiness = None
    unsafe_artifacts = _unsafe_import_artifacts()
    return {
        "dependencies": {name: name not in missing for name in redact.REDACTION_EXTRA_MODULES},
        "ready": readiness_error is None and readiness is not None,
        "readiness": readiness.to_dict() if readiness is not None else None,
        "readiness_error": readiness_error,
        "evidence_source": settings.init.evidence_source,
        "unsafe_artifacts": unsafe_artifacts,
    }


def _unsafe_import_artifacts() -> list[str]:
    if not TRACE_IMPORTS_DIR.exists():
        return []
    return [
        str(cli_output.display_path(artifact))
        for artifact in sorted(TRACE_IMPORTS_DIR.glob("*.jsonl"))
        if not safe_import_artifact(artifact)
    ]


def _redaction_doctor_findings(
    report: dict[str, Any],
) -> tuple[list[str], list[str], list[str]]:
    problems: list[str] = []
    warnings: list[str] = []
    next_steps: list[str] = []
    missing = [name for name, present in report["dependencies"].items() if not present]
    trace_source_configured = report["evidence_source"] is not None
    if missing and trace_source_configured:
        warnings.append(
            "trace redaction dependencies missing: "
            + ", ".join(missing)
            + ". Install kensa[redaction]."
        )
    if report["readiness_error"]:
        finding = str(report["readiness_error"])
        if trace_source_configured:
            problems.append(finding)
        else:
            warnings.append(finding)
    if not report["ready"] and trace_source_configured:
        finding = (
            "mandatory trace redaction is not ready; trace import and payload exposure are blocked."
        )
        problems.append(finding)
        next_steps.append("Run kensa init to bootstrap mandatory trace redaction.")
    warnings.extend(
        f"unsafe trace artifact blocked from exposure (re-import required): {artifact}"
        for artifact in report["unsafe_artifacts"]
    )
    if report["unsafe_artifacts"]:
        next_steps.append("Re-import blocked trace artifacts with kensa import.")
    return problems, warnings, next_steps


def _print_doctor_redaction(report: dict[str, Any]) -> None:
    missing = [name for name, present in report["dependencies"].items() if not present]
    if missing:
        cli_output.item(f"Redaction dependencies missing: {', '.join(missing)}", ok=False)
    else:
        cli_output.item("Redaction dependencies: present")
    if report["ready"]:
        readiness = report["readiness"]
        cli_output.item(
            f"Redaction readiness: ready, model {readiness['model']}-{readiness['model_version']}"
        )
    else:
        cli_output.item("Redaction readiness: missing", ok=False)
    for artifact in report["unsafe_artifacts"]:
        cli_output.item(f"Unsafe trace artifact (re-import required): {artifact}", ok=False)


def _doctor_result(args: Any) -> dict[str, Any]:
    problems: list[str] = []
    warnings: list[str] = [_ENV_SAFETY_WARNING]
    next_steps: list[str] = []
    redaction_report = _doctor_redaction_report()
    redaction_problems, redaction_warnings, redaction_next_steps = _redaction_doctor_findings(
        redaction_report
    )
    problems.extend(redaction_problems)
    warnings.extend(redaction_warnings)
    next_steps.extend(redaction_next_steps)
    eval_readiness = _latest_eval_readiness()
    allow_suspicious_harness = bool(getattr(args, "allow_suspicious_harness", False))
    endpoints = _non_local_endpoint_markers()
    if endpoints:
        warnings.append(
            "non-local network endpoint environment variables detected: "
            + ", ".join(sorted(endpoints))
            + ". This is a warning only; verify the harness uses safe local, staging, mocked, "
            "or sandboxed dependencies."
        )

    misplaced_workflows = _misplaced_kensa_workflows()
    if misplaced_workflows:
        problems.append(
            "Kensa workflow is nested under a subproject and GitHub Actions will not run it: "
            + ", ".join(str(cli_output.display_path(path)) for path in misplaced_workflows)
        )
        next_steps.append(
            "Move the Kensa workflow to the repository root .github/workflows/kensa.yml, "
            "or rerun kensa init from the subproject and remove the nested workflow."
        )

    with cli_output.wait_status("Running Kensa smoke check"):
        smoke = _run_persistent_smoke()
    if smoke.returncode != 0:
        if not _persistent_smoke_path().exists():
            problems.append(
                "Persistent smoke test missing: tests/evals/test_kensa_smoke.py. "
                "Run kensa init to restore it."
            )
            next_steps.append(
                "Run kensa init to restore tests/evals/test_kensa_smoke.py, "
                "then rerun kensa doctor."
            )
        else:
            problems.append(
                "Persistent smoke test failed; tests/evals/test_kensa_smoke.py did not pass."
            )
            next_steps.append(
                "Implement tests/evals/conftest.py::kensa_run, wrap the real model/provider "
                "call with kensa.record_llm_call(...), and rerun kensa doctor."
            )
        if smoke.stdout:
            warnings.append(smoke.stdout.strip())
        if smoke.stderr:
            warnings.append(smoke.stderr.strip())
    else:
        if not eval_readiness.evals_ready:
            warnings.append("Evals readiness also requires one passing domain-shaped Kensa eval.")
            next_steps.append(_EVALS_NEXT_STEP)

    authenticity_warnings = _harness_authenticity_warnings()
    if authenticity_warnings:
        if allow_suspicious_harness:
            warnings.extend(
                f"Harness authenticity warning: {warning}" for warning in authenticity_warnings
            )
        else:
            problems.extend(
                f"Harness authenticity check failed: {warning}" for warning in authenticity_warnings
            )
            next_steps.append(
                "Call the real local agent boundary from tests/evals/conftest.py and mock only "
                "external dependencies."
            )

    exit_code = 1 if problems else 0
    harness_ready = smoke.returncode == 0 and not problems
    readiness_recorded = False
    if harness_ready or _settings_path().exists():
        readiness_recorded = _record_harness_readiness(ready=harness_ready, warnings=warnings)

    return {
        "exit_code": exit_code,
        "summary": "Kensa doctor: ready" if exit_code == 0 else "Kensa doctor: not ready",
        "problems": problems,
        "warnings": warnings,
        "next_steps": next_steps,
        "redaction": redaction_report,
        "non_local_endpoint_markers": sorted(endpoints),
        "misplaced_workflows": [str(cli_output.display_path(path)) for path in misplaced_workflows],
        "readiness_recorded": readiness_recorded,
        "harness_authenticity_warnings": authenticity_warnings,
        "harness_readiness": {
            "ready": harness_ready,
            "smoke_eval_count": 1 if smoke.returncode == 0 else 0,
        },
        "evals_readiness": {
            "ready": eval_readiness.evals_ready,
            "passing_eval_count": eval_readiness.domain_eval_count,
        },
        "smoke": {
            "returncode": int(smoke.returncode),
            "stdout": smoke.stdout or "",
            "stderr": smoke.stderr or "",
        },
    }


def _non_local_endpoint_markers() -> set[str]:
    markers: set[str] = set()
    for key, value in os.environ.items():
        if not value or not any(fragment in key.upper() for fragment in ENDPOINT_ENV_KEY_FRAGMENTS):
            continue
        parsed = urlparse(value)
        if parsed.scheme not in {"http", "https"} or not parsed.hostname:
            continue
        if parsed.hostname not in LOCAL_ENDPOINT_HOSTS and not parsed.hostname.endswith(".local"):
            markers.add(key)
    return markers


