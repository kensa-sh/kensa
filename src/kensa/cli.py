def _init_workflow_target() -> tuple[Path, Path | None]:
    cwd = Path.cwd().resolve()
    git_root = _find_git_root(cwd)
    workflow_path = git_root / ".github" / "workflows" / "kensa.yml"
    working_directory = _relative_subproject_path(cwd, git_root)
    return workflow_path, working_directory


def _find_git_root(start: Path) -> Path:
    current = start.resolve()
    for candidate in (current, *current.parents):
        if (candidate / ".git").exists():
            return candidate
    return current


def _relative_subproject_path(cwd: Path, git_root: Path) -> Path | None:
    relative = cwd.resolve().relative_to(git_root.resolve())
    return None if relative == Path(".") else relative


def _misplaced_kensa_workflows() -> list[Path]:
    cwd = Path.cwd().resolve()
    git_root = _find_git_root(cwd)
    root_workflow_dir = git_root / ".github" / "workflows"
    workflows: list[Path] = []
    current = cwd
    while True:
        workflow_dir = current / ".github" / "workflows"
        if workflow_dir == root_workflow_dir:
            return workflows
        workflows.extend(
            candidate
            for filename in _KENSA_WORKFLOW_FILENAMES
            if (candidate := workflow_dir / filename).exists()
        )
        current = current.parent


def _workflow_text(working_directory: Path | None = None) -> str:
    if working_directory is None:
        return WORKFLOW_TEXT
    working_directory_text = working_directory.as_posix()
    uv_lock_path = f"{working_directory_text}/uv.lock"
    defaults = (
        "    defaults:\n"
        "      run:\n"
        f"        working-directory: {json.dumps(working_directory_text)}\n"
    )
    text = _replace_required_workflow_fragment(
        WORKFLOW_TEXT,
        _WORKFLOW_STEPS_MARKER,
        defaults + _WORKFLOW_STEPS_MARKER,
        "top-level steps block",
        count=1,
    )
    return _replace_required_workflow_fragment(
        text,
        _WORKFLOW_HASHFILES_MARKER,
        f"hashFiles({_github_expression_string(uv_lock_path)})",
        "uv lock hashFiles expression",
    )


def _github_expression_string(text: str) -> str:
    return "'" + text.replace("'", "''") + "'"


def _replace_required_workflow_fragment(
    text: str,
    old: str,
    new: str,
    description: str,
    *,
    count: int = -1,
) -> str:
    if old not in text:
        raise RuntimeError(f"WORKFLOW_TEXT no longer contains {description}.")
    return text.replace(old, new, count)


