def _version_callback(ctx: click.Context, _param: click.Parameter, value: bool) -> None:
    if not value or ctx.resilient_parsing:
        return
    click.echo(_get_version())
    ctx.exit()


def _load_startup_dotenv() -> None:
    dotenv_path = _startup_dotenv_path()
    if dotenv_path is None:
        return
    if not dotenv_path.exists():
        click.echo(f"warning: dotenv file not found: {dotenv_path}", err=True)
        return
    load_dotenv(dotenv_path, override=False, interpolate=False)


def _startup_dotenv_path() -> Path | None:
    env_path = os.environ.get(_DOTENV_ENV_VAR)
    if env_path:
        return Path(env_path).expanduser()
    pyproject = _find_pyproject(Path.cwd())
    if pyproject is None:
        return None
    dotenv = _pyproject_dotenv_path(pyproject)
    if dotenv is None:
        return None
    return dotenv if dotenv.is_absolute() else pyproject.parent / dotenv


def _find_pyproject(start: Path) -> Path | None:
    current = start.resolve()
    for candidate in (current, *current.parents):
        pyproject = candidate / "pyproject.toml"
        if pyproject.exists():
            return pyproject
    return None


def _pyproject_dotenv_path(pyproject: Path) -> Path | None:
    data = tomllib.loads(pyproject.read_text())
    tool = data.get("tool")
    if not isinstance(tool, dict):
        return None
    kensa = tool.get("kensa")
    if not isinstance(kensa, dict):
        return None
    declared = kensa.get("dotenv")
    if not isinstance(declared, str) or not declared:
        return None
    return Path(declared).expanduser()


@click.group(
    cls=KensaGroup,
    context_settings=CONTEXT_SETTINGS,
    invoke_without_command=True,
)
@click.option(
    "--version",
    is_flag=True,
    callback=_version_callback,
    expose_value=False,
    is_eager=True,
    help="Show version.",
)
@click.pass_context
def cli(ctx: click.Context) -> None:
    """Pytest evals from agent traces."""
    _load_startup_dotenv()
    if ctx.invoked_subcommand is None:
        click.echo(ctx.get_help())
        ctx.exit(2)


def main(argv: list[str] | None = None) -> int:
    try:
        result = cli.main(args=argv, prog_name="kensa", standalone_mode=False)
    except click.exceptions.Exit as exc:
        return int(exc.exit_code)
    except click.ClickException as exc:
        exc.show()
        return int(exc.exit_code)
    except KeyboardInterrupt:
        click.echo("Aborted!", err=True)
        return 130
    return int(result or 0)


