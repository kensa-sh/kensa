def _write_if_missing(path: Path, text: str) -> bool:
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists():
        return False
    path.write_text(text)
    return True


def _write_if_missing_or_kensa_generated(
    path: Path,
    text: str,
    is_kensa_generated: Callable[[str], bool],
) -> bool:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not path.exists():
        path.write_text(text)
        return True
    existing = path.read_text()
    if not is_kensa_generated(existing) or existing == text:
        return False
    path.write_text(text)
    return True


def _copy_skill_template_tree(skill_path: Path) -> list[Path]:
    template_root = _skill_template_root()
    written: list[Path] = []
    target_root = skill_path.parent.parent
    for skill_name in _PACKAGED_SKILLS:
        packaged_skill = template_root / skill_name
        skill_template = packaged_skill / "SKILL.md"
        if not skill_template.is_file():
            raise RuntimeError(f"Packaged {skill_name} skill template is missing SKILL.md.")
        destination = target_root / skill_name
        if destination.exists():
            _remove_stale_skill_template_paths(packaged_skill, destination)
        written.extend(_copy_skill_template_files(packaged_skill, destination))
    return written


def _skill_template_root() -> Traversable:
    template_root = files("kensa").joinpath(*_SKILL_TEMPLATE_ROOT)
    if not template_root.is_dir():
        template_path = "/".join(("kensa", *_SKILL_TEMPLATE_ROOT))
        raise RuntimeError(f"Packaged Kensa skill template tree is missing: {template_path}.")
    return template_root


def _copy_skill_template_files(source: Traversable, destination: Path) -> list[Path]:
    written: list[Path] = []
    for item in sorted(source.iterdir(), key=lambda candidate: candidate.name):
        if item.is_dir():
            written.extend(_copy_skill_template_files(item, destination / item.name))
        elif item.is_file():
            path = destination / item.name
            if _write_text_if_changed(path, item.read_text(encoding="utf-8")):
                written.append(path)
    return written


def _remove_stale_skill_template_paths(source: Traversable, destination: Path) -> None:
    references = destination / "references"
    if references.exists() and not _template_tree_has_files(source / "references"):
        shutil.rmtree(references)


def _template_tree_has_files(source: Traversable) -> bool:
    if not source.is_dir():
        return False
    return any(item.is_file() or _template_tree_has_files(item) for item in source.iterdir())


def _write_text_if_changed(path: Path, text: str) -> bool:
    path.parent.mkdir(parents=True, exist_ok=True)
    if path.exists() and path.read_text() == text:
        return False
    path.write_text(text)
    return True


