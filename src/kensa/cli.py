def _validated_dotenv_value(name: str, value: str) -> str:
    if "\n" in value or "\r" in value:
        raise ValueError(f"{name} cannot contain newlines")
    if not value:
        raise ValueError(f"{name} cannot be empty")
    if "'" in value:
        raise ValueError(f"{name} cannot contain single quotes")
    return value


def _write_dotenv_values(path: Path, values: dict[str, str]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    lines = path.read_text().splitlines() if path.exists() else []
    updated: set[str] = set()
    for index, line in enumerate(lines):
        key = line.split("=", 1)[0].strip()
        if key in values:
            lines[index] = _dotenv_line(key, values[key])
            updated.add(key)
    for key, value in values.items():
        if key not in updated:
            lines.append(_dotenv_line(key, value))
    path.write_text("\n".join(lines) + "\n")


def _dotenv_line(key: str, value: str) -> str:
    escaped = value.replace("\\", "\\\\")
    return f"{key}='{escaped}'"


