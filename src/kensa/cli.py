class KensaGroup(click.Group):
    """Custom group that displays the kensa banner in help."""

    def list_commands(self, ctx: click.Context) -> list[str]:
        commands = super().list_commands(ctx)
        ordered = [command for command in _COMMAND_ORDER if command in commands]
        remaining = [command for command in commands if command not in ordered]
        return ordered + remaining

    def format_help(self, ctx: click.Context, formatter: click.HelpFormatter) -> None:
        formatter.write("\n")
        formatter.write(click.style(_cli_logo_with_version(), bold=True))
        formatter.write("\n\n")
        super().format_help(ctx, formatter)


def _get_version() -> str:
    try:
        return importlib.metadata.version("kensa")
    except importlib.metadata.PackageNotFoundError:
        return "dev"


def _cli_logo_with_version() -> str:
    version_tag = f" v{_get_version()} "
    lines = CLI_LOGO.splitlines()
    bottom = lines[-1]
    inner_width = len(bottom) - 2
    if len(version_tag) > inner_width:
        return CLI_LOGO
    border = bottom[1:-1]
    versioned_bottom = (
        f"{bottom[0]}{border[: inner_width - len(version_tag)]}{version_tag}{bottom[-1]}"
    )
    return "\n".join([*lines[:-1], versioned_bottom])


