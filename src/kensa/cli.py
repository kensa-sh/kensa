def _select_interactive_choice(
    label: str,
    *,
    choices: tuple[str, ...],
    default: str,
    choice_label: Callable[[str], str] | None = None,
    help_text: str = _AGENT_SKILL_HELP_TEXT,
) -> str:
    if not _is_interactive():
        _print_choice_help(help_text)
        return click.prompt(
            label,
            type=click.Choice(choices, case_sensitive=False),
            default=default,
            show_choices=True,
        )
    render_label = choice_label or _agent_instruction_label
    selected = choices.index(default) if default in choices else 0
    rendered_lines = _render_choice_menu(label, choices, selected, render_label, help_text)
    while True:
        key = click.getchar(echo=False)
        if key in _CHOICE_DOWN_KEYS:
            selected = (selected + 1) % len(choices)
        elif key in _CHOICE_UP_KEYS:
            selected = (selected - 1) % len(choices)
        elif key in _CHOICE_ENTER_KEYS:
            cli_output.CONSOLE.print()
            return choices[selected]
        else:
            continue
        cli_output.CONSOLE.print(f"\x1b[{rendered_lines}A\x1b[J", end="")
        rendered_lines = _render_choice_menu(label, choices, selected, render_label, help_text)


def _render_choice_menu(
    label: str,
    choices: tuple[str, ...],
    selected: int,
    choice_label: Callable[[str], str],
    help_text: str,
) -> int:
    lines = 0
    cli_output.CONSOLE.print(f"◇  {cli_output.rich_escape(label)}", highlight=False)
    lines += 1
    lines += _print_choice_help(help_text)
    cli_output.CONSOLE.print("│  Use ↑/↓, then Enter.", style="dim", highlight=False)
    lines += 1
    for index, choice in enumerate(choices):
        marker = ">" if index == selected else " "
        style = "bold cyan" if index == selected else "dim"
        rendered_choice = choice_label(choice)
        cli_output.CONSOLE.print(
            f"│  {marker} {cli_output.rich_escape(rendered_choice)}", style=style, highlight=False
        )
        lines += 1
    return lines


def _print_choice_help(
    help_text: str = _AGENT_SKILL_HELP_TEXT,
) -> int:
    cli_output.CONSOLE.print(
        f"│  {cli_output.rich_escape(help_text)}",
        style="dim",
        highlight=False,
    )
    return 1


def _agent_instruction_targets_for_choice(
    choice: AgentInstructionChoice,
) -> tuple[tuple[AgentInstruction, Path], ...]:
    if choice == "all":
        return tuple(
            (cast(AgentInstruction, key), path) for key, path in _AGENT_INSTRUCTION_PATHS.items()
        )
    path = _agent_instruction_path_for_choice(choice)
    if path is None:
        return ()
    if choice == "other":
        return (("other", path),)
    key = _agent_instruction_key_for_path(path)
    assert key is not None
    return ((cast(AgentInstruction, key), path),)


def _agent_instruction_path_for_choice(choice: str) -> Path | None:
    if choice == "other":
        return _agent_instruction_path("codex")
    if choice == "auto":
        detected = _detected_agent_instruction_key()
        return _agent_instruction_path(detected) if detected is not None else None
    return _agent_instruction_path(choice)


