def _check_connection(args: Any, metadata: dict[str, Any]) -> _ConnectionCheck:
    provider = str(args.provider)
    endpoint = str(metadata.get("endpoint") or "")
    if provider == "langfuse":
        raw_auth = metadata.get("auth")
        auth: dict[str, Any] = raw_auth if isinstance(raw_auth, dict) else {}
        public_key_env = str(auth.get("public_key_env") or "LANGFUSE_PUBLIC_KEY")
        secret_key_env = str(auth.get("secret_key_env") or "LANGFUSE_SECRET_KEY")
        check = _ConnectionCheck(
            endpoint=endpoint,
            credential_envs=(public_key_env, secret_key_env),
        )
        values = _required_env_values(
            (public_key_env, secret_key_env),
            provider="Langfuse",
        )
        public_key = values[public_key_env]
        secret_key = values[secret_key_env]
        try:
            check_langfuse_connection(
                endpoint=endpoint,
                public_key=public_key,
                secret_key=secret_key,
            )
        except ValueError as exc:
            check = replace(check, endpoint_reachable=getattr(exc, "status_code", None) is not None)
            raise _ConnectionCheckError(str(exc), check=check) from exc
        return replace(check, endpoint_reachable=True, authenticated=True)
    raise ValueError(f"unsupported connection provider: {provider}")


def _cmd_import(args: Any) -> int:
    json_output = bool(getattr(args, "json", False))
    provider = str(args.provider).lower()
    if provider not in {"json", "jsonl", "otlp", "langfuse"}:
        return _import_argument_error(
            f"Unsupported import provider: {args.provider}",
            json_output=json_output,
        )
    source_mode = "file" if args.source else "connected"
    if not args.source and provider != "langfuse":
        return _import_argument_error(
            "Connected imports are only implemented for Langfuse; pass --source.",
            json_output=json_output,
        )
    connected_only_options = [
        option
        for option, value in (
            ("--endpoint", getattr(args, "endpoint", None)),
            ("--since", getattr(args, "since", None)),
        )
        if args.source and value is not None
    ]
    if connected_only_options:
        return _import_argument_error(
            f"{', '.join(connected_only_options)} can only be used with connected imports, "
            "not with --source.",
            json_output=json_output,
        )
    args.limit = _resolve_import_limit(
        provider=provider,
        source=getattr(args, "source", None),
        limit=getattr(args, "limit", None),
    )
    timestamp = _unix_timestamp()
    out = TRACE_IMPORTS_DIR / f"{provider}-{timestamp}.jsonl"
    try:
        if args.source:
            result = import_trace_source(
                provider=provider,
                source=args.source,
                out=out,
                limit=args.limit,
                max_payload_bytes=args.max_payload_bytes,
            )
        else:
            result = _connected_import(args, out=out)
    except (OSError, RuntimeError, ValueError) as exc:
        if json_output:
            cli_output.print_json_envelope(
                command="import",
                ok=False,
                exit_code=1,
                summary="Kensa import failed.",
                errors=[str(exc)],
            )
        else:
            cli_output.item(f"error: {exc}", ok=False, err=True)
        return 1
    latest_path = _write_trace_import_latest(
        result,
        timestamp=timestamp,
        source_mode=source_mode,
    )
    if json_output:
        _print_import_json(
            result,
            latest_path=latest_path,
            timestamp=timestamp,
            source_mode=source_mode,
        )
    else:
        cli_output.step("kensa import")
        cli_output.item(f"provider: {result.provider}")
        cli_output.item(f"records written: {result.records_written}")
        cli_output.item(f"spans written: {result.span_count}")
        cli_output.item(f"artifact: {cli_output.display_path(result.out_path)}")
        if result.manifest_path:
            cli_output.item(f"manifest: {cli_output.display_path(result.manifest_path)}")
        cli_output.item(f"latest: {cli_output.display_path(latest_path)}")
        for warning in result.warnings:
            cli_output.notice(warning)
    return 0


def _resolve_import_limit(*, provider: str, source: str | None, limit: int | None) -> int:
    if limit is not None:
        return limit
    if provider == "langfuse" and source is None:
        return _LANGFUSE_CONNECTED_DEFAULT_LIMIT
    return _IMPORT_DEFAULT_LIMIT


def _connected_import(
    args: Any,
    *,
    out: Path,
) -> ImportResult:
    provider = str(args.provider)
    connection = _load_connection(provider)
    endpoint = str(getattr(args, "endpoint", None) or connection.get("endpoint") or "")
    if not endpoint:
        raise ValueError(f"{provider} connection is missing an endpoint")
    if provider == "langfuse":
        redactor = redact.Redactor()
        payload = _connected_langfuse_payload(
            connection,
            endpoint=endpoint,
            since=getattr(args, "since", None),
            limit=int(args.limit),
            import_mode=cast(
                LangfuseImportMode,
                getattr(args, "langfuse_mode", "auto"),
            ),
        )
    else:
        raise ValueError(f"unsupported connected import provider: {provider}")
    with cli_output.wait_status("Writing trace import"):
        # Redact fetched payloads in memory; raw connected payloads never transit disk.
        return _import_trace_records(
            provider=provider,
            payload=payload,
            source_label=f"{provider}:connected",
            out=out,
            limit=int(args.limit),
            max_payload_bytes=int(args.max_payload_bytes),
            redactor=redactor,
        )


def _load_connection(provider: str) -> dict[str, Any]:
    path = _CONNECTIONS_DIR / f"{provider}.json"
    if not path.exists():
        raise ValueError(f"No {provider} connection found. Run kensa connect {provider} first.")
    payload = json.loads(path.read_text())
    if not isinstance(payload, dict) or payload.get("provider") != provider:
        raise ValueError(
            f"Invalid {provider} connection metadata at {cli_output.display_path(path)}"
        )
    return payload


def _connected_langfuse_payload(
    connection: dict[str, Any],
    *,
    endpoint: str,
    since: str | None,
    limit: int,
    import_mode: LangfuseImportMode = "auto",
) -> dict[str, Any]:
    raw_auth = connection.get("auth")
    auth: dict[str, Any] = raw_auth if isinstance(raw_auth, dict) else {}
    public_key_env = str(auth.get("public_key_env") or "LANGFUSE_PUBLIC_KEY")
    secret_key_env = str(auth.get("secret_key_env") or "LANGFUSE_SECRET_KEY")
    public_key = _required_env(public_key_env, provider="Langfuse")
    secret_key = _required_env(secret_key_env, provider="Langfuse")
    with cli_output.wait_status("Fetching Langfuse traces"):
        return fetch_langfuse_connected_export(
            endpoint=endpoint,
            since=since,
            limit=limit,
            public_key=public_key,
            secret_key=secret_key,
            import_mode=import_mode,
        )


def _required_env_values(names: tuple[str, ...], *, provider: str) -> dict[str, str]:
    values = {name: os.environ.get(name) for name in names}
    missing = [name for name, value in values.items() if not value]
    if missing:
        rendered = ", ".join(missing)
        raise ValueError(f"Missing {provider} credentials: set {rendered}.")
    return {name: str(value) for name, value in values.items()}


def _required_env(name: str, *, provider: str) -> str:
    return _required_env_values((name,), provider=provider)[name]


def _write_trace_import_latest(
    result: ImportResult,
    *,
    timestamp: int,
    source_mode: str,
) -> Path:
    latest_path = TRACE_IMPORTS_DIR / "latest.json"
    payload = {
        "schema_version": TRACE_IMPORT_LATEST_SCHEMA_VERSION,
        "provider": result.provider,
        "source_mode": source_mode,
        "artifact_path": str(cli_output.display_path(result.out_path)),
        "manifest_path": str(cli_output.display_path(result.manifest_path))
        if result.manifest_path
        else None,
        "timestamp": timestamp,
        "created_at": _utc_now_iso(),
        "records_written": result.records_written,
        "span_count": result.span_count,
    }
    latest_path.parent.mkdir(parents=True, exist_ok=True)
    latest_path.write_text(json.dumps(payload, indent=2, sort_keys=True) + "\n")
    return latest_path


