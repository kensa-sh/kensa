def _detected_agent_instruction_key() -> str | None:
    candidates = [
        (key, markers)
        for key, binaries, _path, markers in _AGENT_INSTRUCTION_TARGETS
        if any(shutil.which(binary) for binary in binaries)
    ]
    marker_matches = [
        key for key, markers in candidates if any(marker.exists() for marker in markers)
    ]
    if len(marker_matches) == 1:
        return marker_matches[0]
    if candidates:
        return candidates[0][0]
    return None


