def _split_eval_tokens(tokens: tuple[str, ...]) -> tuple[list[str], list[str]]:
    paths: list[str] = []
    pytest_args: list[str] = []
    passthrough = False
    for token in tokens:
        if token == "--":
            passthrough = True
            continue
        if passthrough or token.startswith("-"):
            passthrough = True
            pytest_args.append(token)
        else:
            paths.append(token)
    return paths or list(DEFAULT_EVAL_PATHS), pytest_args


class _Steps:
    """Clack-inspired step output for interactive terminal UX."""

    def __init__(self) -> None:
        self._console = cli_output.CONSOLE

    def start(self, label: str) -> None:
        self._console.print()
        self._console.print(_cli_logo_with_version(), style="bold", highlight=False)
        self._console.print()
        self._console.print(f"┌  {cli_output.rich_escape(label)}", highlight=False)
        self._console.print("│", highlight=False)

    def step(self, label: str) -> None:
        self._console.print(f"◇  {cli_output.rich_escape(label)}", highlight=False)

    def item(self, text: str, *, ok: bool = True) -> None:
        color = "green" if ok else "red"
        marker = "✓" if ok else "✗"
        self._console.print(
            f"│  [{color}]{marker}[/{color}] {cli_output.rich_escape(text)}", highlight=False
        )

    def notice(self, text: str) -> None:
        self._console.print(
            f"│  [yellow]![/yellow] {cli_output.rich_escape(text)}", highlight=False
        )

    def text(self, text: str, *, soft_wrap: bool = False) -> None:
        self._console.print(
            f"│  {cli_output.rich_escape(text)}", highlight=False, soft_wrap=soft_wrap
        )

    def end(self, text: str = "Done!") -> None:
        self._console.print("│", highlight=False)
        self._console.print(f"└  {text}", highlight=False)
        self._console.print()


def _cmd_eval(args: Any, pytest_args: list[str]) -> int:
    paths = args.paths or list(DEFAULT_EVAL_PATHS)
    json_output = bool(getattr(args, "json", False))
    cmd = [
        sys.executable,
        "-m",
        "pytest",
        *paths,
        "--kensa-write-artifacts",
        "--kensa-report=json" if json_output else "--kensa-report=term",
    ]
    if args.no_judge:
        cmd.append("--kensa-no-judge")
    cmd.extend(pytest_args)
    if json_output:
        with cli_output.wait_status("Running Kensa evals"):
            completed = subprocess.run(
                cmd,
                check=False,
                capture_output=True,
                text=True,
            )
    else:
        completed = subprocess.run(cmd, check=False, capture_output=False, text=False)
    artifact = _latest_result_artifact(Path.cwd() / ".kensa" / "results")
    warnings: list[str] = []
    errors: list[str] = []
    next_steps: list[str] = []
    json_report = getattr(args, "json_report", None)
    markdown_report = getattr(args, "markdown_report", None)
    if json_report and artifact is not None:
        shutil.copyfile(artifact, json_report)
    if markdown_report and artifact is not None:
        _write_markdown_report(artifact, Path(markdown_report))
    exit_code = int(completed.returncode)
    artifact_payload: dict[str, Any] | None = None
    if artifact is not None:
        try:
            artifact_payload = json.loads(artifact.read_text())
        except (OSError, json.JSONDecodeError) as exc:
            warnings.append(f"Could not read eval artifact {artifact}: {exc}")
    readiness = _eval_readiness(artifact_payload)
    if artifact_payload is not None and readiness.trace_artifact is None:
        run_id = artifact_payload.get("run_id")
        if isinstance(run_id, str) and run_id:
            expected = Path.cwd() / ".kensa" / "traces" / "runs" / run_id / "trials.jsonl"
            warnings.append(
                f"Trace artifact could not be found at {cli_output.display_path(expected)}."
            )
    eval_problem = _eval_problem(exit_code, paths)
    errors.extend(eval_problem.errors)
    next_steps.extend(eval_problem.next_steps)
    final_exit_code = exit_code
    summary = eval_problem.summary
    if exit_code == 0 and readiness.smoke_only:
        warnings.append(_SMOKE_ONLY_WARNING)
        next_steps.append(_EVALS_NEXT_STEP)
        errors.append("Evals readiness is missing: only the readiness smoke passed.")
        final_exit_code = 1
        summary = "Kensa eval passed, but evals readiness is missing."
    elif exit_code == 0 and not readiness.evals_ready:
        errors.append("Evals readiness is missing: no passing domain evals were found.")
        next_steps.append(_EVALS_NEXT_STEP)
        final_exit_code = 1
        summary = "Kensa eval passed, but evals readiness is missing."
    if json_output:
        cli_output.print_json_envelope(
            command="eval",
            ok=final_exit_code == 0,
            exit_code=final_exit_code,
            summary=summary,
            data={
                "paths": paths,
                "pytest_args": pytest_args,
                "pytest_command": cmd,
                "artifact": str(artifact) if artifact else None,
                "trace_artifact": (
                    str(cli_output.display_path(readiness.trace_artifact))
                    if readiness.trace_artifact is not None
                    else None
                ),
                "json_report": json_report,
                "markdown_report": markdown_report,
                "run_id": artifact_payload.get("run_id") if artifact_payload else None,
                "aggregates": artifact_payload.get("aggregates", []) if artifact_payload else [],
                **_eval_readiness_data(readiness),
                "pytest": {
                    "returncode": exit_code,
                    "stdout": getattr(completed, "stdout", "") or "",
                    "stderr": getattr(completed, "stderr", "") or "",
                },
            },
            warnings=warnings,
            errors=errors,
            next_steps=next_steps,
        )
    elif exit_code == _PYTEST_NO_TESTS_COLLECTED:
        cli_output.step("kensa eval")
        cli_output.item(eval_problem.errors[0], ok=False, err=True)
        cli_output.print_next_steps(eval_problem.next_steps)
    elif exit_code == 0:
        _print_eval_readiness_terminal(readiness, warnings, errors, next_steps)
    else:
        cli_output.print_next_steps(next_steps)
    return final_exit_code


def _eval_readiness(artifact_payload: dict[str, Any] | None) -> _EvalReadiness:
    harness_smoke_count = 0
    domain_eval_count = 0
    trace_artifact: Path | None = None
    if artifact_payload is None:
        return _EvalReadiness(
            harness_smoke_count=harness_smoke_count,
            domain_eval_count=domain_eval_count,
            trace_artifact=trace_artifact,
        )
    aggregates = artifact_payload.get("aggregates")
    if isinstance(aggregates, list):
        for aggregate in aggregates:
            if not isinstance(aggregate, dict) or aggregate.get("verdict") != "pass":
                continue
            if _is_scaffolded_smoke_aggregate(aggregate):
                harness_smoke_count += 1
            else:
                domain_eval_count += 1
    run_id = artifact_payload.get("run_id")
    if isinstance(run_id, str) and run_id:
        candidate = Path.cwd() / ".kensa" / "traces" / "runs" / run_id / "trials.jsonl"
        if candidate.exists():
            trace_artifact = candidate
    return _EvalReadiness(
        harness_smoke_count=harness_smoke_count,
        domain_eval_count=domain_eval_count,
        trace_artifact=trace_artifact,
    )


def _eval_readiness_data(readiness: _EvalReadiness) -> dict[str, Any]:
    return {
        "harness_readiness": {
            "ready": readiness.harness_ready,
            "smoke_eval_count": readiness.harness_smoke_count,
        },
        "evals_readiness": {
            "ready": readiness.evals_ready,
            "passing_eval_count": readiness.domain_eval_count,
        },
    }


def _latest_eval_readiness() -> _EvalReadiness:
    artifact = _latest_result_artifact(Path.cwd() / ".kensa" / "results")
    if artifact is None:
        return _eval_readiness(None)
    try:
        payload = json.loads(artifact.read_text())
    except (OSError, json.JSONDecodeError):
        return _eval_readiness(None)
    return _eval_readiness(payload if isinstance(payload, dict) else None)


def _is_scaffolded_smoke_aggregate(aggregate: dict[str, Any]) -> bool:
    group_id = str(aggregate.get("group_id") or "")
    return aggregate.get("case_id") == SMOKE_CASE_ID or (SMOKE_NODEID_FRAGMENT in group_id)


def _print_eval_readiness_terminal(
    readiness: _EvalReadiness,
    warnings: list[str],
    errors: list[str],
    next_steps: list[str],
) -> None:
    if readiness.trace_artifact is not None:
        cli_output.notice(f"Trace artifact: {cli_output.display_path(readiness.trace_artifact)}")
    for warning in warnings:
        if warning == _SMOKE_ONLY_WARNING:
            continue
        cli_output.notice(warning)
    if not readiness.smoke_only and not errors and not next_steps:
        return
    cli_output.step("Evals readiness" if readiness.smoke_only else "kensa eval")
    if readiness.smoke_only:
        cli_output.notice(_SMOKE_ONLY_WARNING)
    for error in errors:
        cli_output.item(error, ok=False, err=True)
    cli_output.print_next_steps(next_steps)


def _eval_problem(exit_code: int, paths: list[str]) -> _EvalProblem:
    if exit_code == 0:
        return _EvalProblem(summary="Kensa eval passed.", errors=[], next_steps=[])
    if exit_code == _PYTEST_NO_TESTS_COLLECTED:
        path_text = ", ".join(paths)
        return _EvalProblem(
            summary="Kensa eval found no eval tests.",
            errors=[f"No Kensa eval tests were collected from {path_text}."],
            next_steps=[
                "Run kensa doctor to verify tests/evals/conftest.py::kensa_run(case).",
                _EVALS_NEXT_STEP,
            ],
        )
    return _EvalProblem(
        summary="Kensa eval failed.",
        errors=[],
        next_steps=["Inspect the artifact or report for failing aggregates."],
    )


def _latest_result_artifact(result_dir: Path) -> Path | None:
    if not result_dir.exists():
        return None
    files = sorted(result_dir.glob("*.json"), key=lambda path: path.stat().st_mtime)
    return files[-1] if files else None


def _write_markdown_report(source: Path, destination: Path) -> None:
    data = json.loads(source.read_text())
    lines = ["# Kensa Eval Report", ""]
    lines.extend(
        (
            f"- **{aggregate['verdict']}** `{aggregate['group_id']}`: "
            f"{aggregate['passed']}/{aggregate['total']} passed"
        )
        for aggregate in data.get("aggregates", [])
    )
    destination.write_text("\n".join(lines) + "\n")


def _cmd_doctor(args: Any) -> int:
    result = _doctor_result(args)
    if bool(getattr(args, "json", False)):
        cli_output.print_json_envelope(
            command="doctor",
            ok=result["exit_code"] == 0,
            exit_code=int(result["exit_code"]),
            summary=str(result["summary"]),
            data={
                "redaction": result["redaction"],
                "non_local_endpoint_markers": result["non_local_endpoint_markers"],
                "misplaced_workflows": result["misplaced_workflows"],
                "readiness_recorded": result["readiness_recorded"],
                "harness_authenticity_warnings": result["harness_authenticity_warnings"],
                "harness_readiness": result["harness_readiness"],
                "evals_readiness": result["evals_readiness"],
                "smoke": result["smoke"],
            },
            warnings=list(result["warnings"]),
            errors=list(result["problems"]),
            next_steps=list(result["next_steps"]),
        )
        return int(result["exit_code"])

    next_steps = list(result["next_steps"])
    cli_output.step("kensa doctor")
    _print_doctor_redaction(result["redaction"])
    for warning in result["warnings"]:
        cli_output.item(f"warning: {warning}", ok=False)
    if result["problems"]:
        for problem in result["problems"]:
            cli_output.item(f"error: {problem}", ok=False, err=True)
        cli_output.print_next_steps(next_steps)
        cli_output.ERR_CONSOLE.print("[red]Kensa doctor: not ready[/red]")
        return int(result["exit_code"])
    cli_output.item("Harness readiness: ready")
    if result["evals_readiness"]["ready"]:
        cli_output.item("Evals readiness: ready")
    else:
        cli_output.item("Evals readiness: missing", ok=False)
    cli_output.item("Kensa doctor: ready")
    if result["readiness_recorded"]:
        cli_output.item("Readiness recorded locally.")
    cli_output.print_next_steps(next_steps)
    return int(result["exit_code"])


