def _dotenv_has_key(path: Path, key: str) -> bool:
    if not path.exists():
        return False
    for line in path.read_text().splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#") or "=" not in stripped:
            continue
        if stripped.split("=", 1)[0].strip() == key:
            return True
    return False


