    steps.step("Finish setup")
    steps.text("")
    steps.text("Next steps:")
    next_steps = _agent_skill_steps(instruction_key)
    for index, step in enumerate(next_steps, start=1):
        steps.text(f"  {index}. {step}", soft_wrap=True)
    steps.text("")


